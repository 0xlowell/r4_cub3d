#include "../../utils/inc/libft.h"

void	free_tab(void **tab)
{
	int	i;

	i = 0;
	if (tab)
	{
		while (tab[i])
			i++;
		while (i >= 0)
			free(tab[i--]);
		free(tab);
		tab = NULL;
	}
}
