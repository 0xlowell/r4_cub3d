int	ft_str_isdigit(char *s, char c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if ((s[i] >= '0' && s[i] <= '9') || s[i] == c)
			i++;
		else
			return (0);
	}
	return (1);
}
